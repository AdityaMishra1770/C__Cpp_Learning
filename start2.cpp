#include<iostream>
using namespace std;
int main(int argc, char const *argv[]){
    // Bitwise Operator :
    ///Bitwise compares every single binary place according to operand and results acc. to truth table
    cout<<(2&4)<<endl; 
    cout<<(2&&4)<<endl; 
    cout<<(2|4)<<endl; 
    cout<<(2||4)<<endl; 
    cout<<(~10)<<endl; 
    return 0;
}